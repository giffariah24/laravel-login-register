    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $request->validate([
            'name' => 'required|string|min:5',
            'email' => 'required|string|email',
            'password' => 'required|confirmed|min:6|regex:/[a-z]/|regex:/[A-Z]/|regex:/[0-9]/',
            'password_confirmation' => 'required',
            'avatar' => 'required|mimes:jpeg,jpg,png|max:1024',
        ]);

        $data = [
            'name' => $request->name,
            'email' => $request->email,
            'password' => bcrypt($request->password),
            'avatar' => $request->file('avatar')->store('avatar')
        ];

        User::create($data);

        return Redirect('/register')->with(['success' => "Registration Success"]);
    }

